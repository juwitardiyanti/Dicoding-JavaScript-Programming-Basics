// Membuat variabel (konstan) bernama `currency` dan isi dengan nilai "IDR".
const currency = "IDR";

// Membuat variabel bernama `value` dan isi dengan nilai 10000.
let value = 10000;

// Menambahkan nilai di dalam variabel `value` sebesar 5000.
value += 5000;

/**
 * Membuat variabel (konstan) bernama `money`,
 * Mengisi dengan penambahan string dari nilai `currency` + " " + `value`.
 */
const money = currency + " " + value;

console.log(money); 
